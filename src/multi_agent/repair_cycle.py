"""Repair Cycle — structured Diagnose-Localize-Repair (DLR) for retry loops.

Replaces the simple "send feedback back to builder" retry with structured
root-cause analysis, fault localization, and targeted repair instructions.

Inspired by:
- MapCoder (ACL 2024): explicit debug agent with targeted repair
- RepairAgent: automated program repair with fault localization
- SWE-agent (NeurIPS 2024): structure-aware code navigation

Novel contribution: DLR cycle on top of IDE-agent orchestration where
diagnosis, localization, and repair instructions are generated by the
orchestrator (not the agents), providing a structured repair plan that
works with any black-box IDE agent.
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field
from enum import StrEnum
from typing import Any

from multi_agent.verifier import VerificationResult


# ── Enums ────────────────────────────────────────────────

class FailureCategory(StrEnum):
    """Categorization of build/test failures for targeted repair.

    Each category maps to a different repair strategy:
    - TEST_FAILURE: specific test assertions fail, fix the code under test.
    - LOGIC_ERROR: code runs but produces wrong results.
    - MISSING_REQUIREMENT: a spec requirement was not implemented.
    - INTERFACE_MISMATCH: function signatures, API contracts, or types wrong.
    - QUALITY_GATE: passes functionally but fails lint/coverage/style.
    - STYLE_ISSUE: cosmetic issues only (naming, formatting).
    - UNKNOWN: cannot determine category from available signals.
    """

    TEST_FAILURE = "test_failure"
    LOGIC_ERROR = "logic_error"
    MISSING_REQUIREMENT = "missing_requirement"
    INTERFACE_MISMATCH = "interface_mismatch"
    QUALITY_GATE = "quality_gate"
    STYLE_ISSUE = "style_issue"
    UNKNOWN = "unknown"


# ── Data Classes ─────────────────────────────────────────

@dataclass
class DiagnosisReport:
    """Result of the Diagnose phase.

    Attributes:
        category: The classified failure category.
        root_cause_summary: One-sentence description of what went wrong.
        evidence: Supporting evidence strings (error messages, test names).
        confidence: Confidence in the diagnosis, 0.0 to 1.0.
    """

    category: FailureCategory
    root_cause_summary: str
    evidence: list[str] = field(default_factory=list)
    confidence: float = 0.5


@dataclass
class RepairTarget:
    """A specific location and issue identified for repair.

    Attributes:
        file_path: Path to the file needing repair.
        function_name: Name of the function/method, if identified.
        line_range: Tuple of (start, end) line numbers, if identified.
        issue: Description of what is wrong at this location.
        suggested_fix: Hint for how to fix the issue.
    """

    file_path: str
    function_name: str | None = None
    line_range: tuple[int, int] | None = None
    issue: str = ""
    suggested_fix: str = ""


@dataclass
class RepairPlan:
    """Complete repair plan combining diagnosis, targets, and strategy.

    Attributes:
        diagnosis: The diagnosis that motivated this plan.
        targets: Ordered list of repair targets (by priority).
        strategy: Either "incremental" (patch existing) or "fresh_start"
            (rewrite the failing component).
        priority_order: Indices into targets list for execution order.
    """

    diagnosis: DiagnosisReport
    targets: list[RepairTarget] = field(default_factory=list)
    strategy: str = "incremental"
    priority_order: list[int] = field(default_factory=list)


# ── Pattern Matching Helpers ─────────────────────────────

# Matches pytest FAILED lines like "FAILED tests/test_foo.py::test_bar"
_FAILED_TEST_RE = re.compile(
    r"FAILED\s+(?P<file>[^\s:]+)::(?P<test>[^\s]+)"
)

# Matches assertion errors with optional message
_ASSERT_RE = re.compile(
    r"(?:AssertionError|assert)\s*(?P<detail>.*)",
    re.IGNORECASE,
)

# Matches file:line references in error output
_FILE_LINE_RE = re.compile(
    r"(?P<file>[a-zA-Z0-9_/\\.-]+\.py)"
    r"(?::(?P<line>\d+))?"
)

# Matches function/method references like "def foo(" or "in foo"
_FUNC_REF_RE = re.compile(
    r"(?:def\s+|in\s+)(?P<func>[a-zA-Z_]\w*)"
)

# Matches import errors
_IMPORT_ERROR_RE = re.compile(
    r"(?:ImportError|ModuleNotFoundError):\s*(?P<detail>.*)"
)

# Matches type errors
_TYPE_ERROR_RE = re.compile(
    r"TypeError:\s*(?P<detail>.*)"
)

# Matches "missing" / "not implemented" language in feedback
_MISSING_RE = re.compile(
    r"(?:missing|not\s+implemented|lacks?|absent|omitted|should\s+(?:have|include))",
    re.IGNORECASE,
)

# Matches interface/signature issues
_INTERFACE_RE = re.compile(
    r"(?:signature|parameter|argument|return\s+type|interface|incompatible|mismatch)",
    re.IGNORECASE,
)

# Matches lint/style references
_LINT_RE = re.compile(
    r"(?:lint|flake8|ruff|pylint|style|formatting|pep\s*8|E\d{3}|W\d{3}|F\d{3})",
    re.IGNORECASE,
)


# ── Diagnose ─────────────────────────────────────────────

def diagnose(
    reviewer_feedback: str,
    verification_result: VerificationResult | None = None,
    builder_output: str = "",
    done_criteria: str = "",
) -> DiagnosisReport:
    """Analyze what went wrong based on feedback and verification signals.

    Uses text pattern matching on test output, reviewer feedback, and
    builder output to classify the failure and extract evidence.

    Args:
        reviewer_feedback: The reviewer's feedback text.
        verification_result: Results from automated verification, if available.
        builder_output: Raw output from the builder agent.
        done_criteria: The task's acceptance criteria for gap analysis.

    Returns:
        A :class:`DiagnosisReport` with classified failure and evidence.
    """
    evidence: list[str] = []
    combined_text = f"{reviewer_feedback}\n{builder_output}"

    # ── Check verification results first (ground truth) ──
    if verification_result and not verification_result.tests_ok:
        # Extract failing test names
        for m in _FAILED_TEST_RE.finditer(verification_result.test_output):
            evidence.append(f"FAILED: {m.group('file')}::{m.group('test')}")

        # Check for import/type errors (interface mismatch)
        import_matches = _IMPORT_ERROR_RE.findall(verification_result.test_output)
        type_matches = _TYPE_ERROR_RE.findall(verification_result.test_output)

        if import_matches or type_matches:
            for m in import_matches:
                evidence.append(f"ImportError: {m}")
            for m in type_matches:
                evidence.append(f"TypeError: {m}")
            return DiagnosisReport(
                category=FailureCategory.INTERFACE_MISMATCH,
                root_cause_summary=(
                    "Import or type errors indicate interface mismatches "
                    "between components."
                ),
                evidence=evidence,
                confidence=0.85,
            )

        # Check for assertion errors (test failure)
        assert_matches = _ASSERT_RE.findall(verification_result.test_output)
        if assert_matches:
            for m in assert_matches[:5]:  # Cap evidence
                evidence.append(f"AssertionError: {m.strip()}")

        return DiagnosisReport(
            category=FailureCategory.TEST_FAILURE,
            root_cause_summary=(
                f"{verification_result.test_failed} test(s) failed, "
                f"{verification_result.test_errors} error(s). "
                "Code does not meet test expectations."
            ),
            evidence=evidence,
            confidence=0.8,
        )

    # ── Check for lint-only failures ─────────────────────
    if verification_result and not verification_result.lint_ok and verification_result.tests_ok:
        evidence.append(f"Lint errors: {verification_result.lint_errors}")
        if verification_result.lint_output:
            for line in verification_result.lint_output.splitlines()[:5]:
                if line.strip():
                    evidence.append(line.strip())

        # Determine if it's style vs quality gate
        category = FailureCategory.STYLE_ISSUE
        if verification_result.lint_errors > 5:
            category = FailureCategory.QUALITY_GATE
            confidence = 0.75
        else:
            confidence = 0.8

        return DiagnosisReport(
            category=category,
            root_cause_summary=(
                f"Code has {verification_result.lint_errors} lint error(s) "
                "that must be fixed."
            ),
            evidence=evidence,
            confidence=confidence,
        )

    # ── Pattern-match reviewer feedback (no verification) ─
    if _MISSING_RE.search(combined_text):
        # Extract what is missing
        for line in reviewer_feedback.splitlines():
            if _MISSING_RE.search(line):
                evidence.append(line.strip())
        return DiagnosisReport(
            category=FailureCategory.MISSING_REQUIREMENT,
            root_cause_summary=(
                "Reviewer indicates missing functionality or unimplemented "
                "requirements."
            ),
            evidence=evidence[:5],
            confidence=0.65,
        )

    if _INTERFACE_RE.search(combined_text):
        for line in reviewer_feedback.splitlines():
            if _INTERFACE_RE.search(line):
                evidence.append(line.strip())
        return DiagnosisReport(
            category=FailureCategory.INTERFACE_MISMATCH,
            root_cause_summary=(
                "Reviewer identified interface or signature mismatches."
            ),
            evidence=evidence[:5],
            confidence=0.6,
        )

    if _LINT_RE.search(combined_text):
        for line in reviewer_feedback.splitlines():
            if _LINT_RE.search(line):
                evidence.append(line.strip())
        return DiagnosisReport(
            category=FailureCategory.QUALITY_GATE,
            root_cause_summary=(
                "Reviewer flagged code quality or style issues."
            ),
            evidence=evidence[:5],
            confidence=0.6,
        )

    # ── Fallback ─────────────────────────────────────────
    # Extract any concrete feedback as evidence
    for line in reviewer_feedback.splitlines()[:10]:
        stripped = line.strip()
        if stripped and len(stripped) > 10:
            evidence.append(stripped)

    return DiagnosisReport(
        category=FailureCategory.UNKNOWN,
        root_cause_summary=(
            "Could not automatically classify the failure. "
            "Reviewer feedback should be examined manually."
        ),
        evidence=evidence[:5],
        confidence=0.3,
    )


# ── Localize ─────────────────────────────────────────────

def localize(
    diagnosis: DiagnosisReport,
    changed_files: list[str],
    verification_result: VerificationResult | None = None,
) -> list[RepairTarget]:
    """Identify specific files and functions that need repair.

    Uses evidence from the diagnosis and verification output to pinpoint
    fault locations. Falls back to listing all changed files if specific
    locations cannot be determined.

    Args:
        diagnosis: The diagnosis report from :func:`diagnose`.
        changed_files: List of files modified by the builder.
        verification_result: Optional verification results for output parsing.

    Returns:
        List of :class:`RepairTarget` ordered by specificity.
    """
    targets: list[RepairTarget] = []
    seen_files: set[str] = set()

    # ── Extract file:line references from evidence ───────
    all_evidence = "\n".join(diagnosis.evidence)
    if verification_result:
        all_evidence += "\n" + verification_result.test_output

    for m in _FILE_LINE_RE.finditer(all_evidence):
        fpath = m.group("file")
        # Only include files that are in the changed set or test files
        matching = [f for f in changed_files if f.endswith(fpath) or fpath.endswith(f)]
        if not matching and "test" not in fpath.lower():
            continue
        resolved = matching[0] if matching else fpath

        if resolved in seen_files:
            continue
        seen_files.add(resolved)

        line = int(m.group("line")) if m.group("line") else None
        line_range = (max(1, line - 5), line + 10) if line else None

        # Try to extract function name near this location
        func_name = None
        context_start = max(0, m.start() - 200)
        context = all_evidence[context_start:m.end() + 200]
        func_match = _FUNC_REF_RE.search(context)
        if func_match:
            func_name = func_match.group("func")

        targets.append(RepairTarget(
            file_path=resolved,
            function_name=func_name,
            line_range=line_range,
            issue=diagnosis.root_cause_summary,
            suggested_fix="",
        ))

    # ── Extract failing test targets ─────────────────────
    if verification_result and not verification_result.tests_ok:
        for m in _FAILED_TEST_RE.finditer(verification_result.test_output):
            test_file = m.group("file")
            test_name = m.group("test")
            if test_file not in seen_files:
                seen_files.add(test_file)
                targets.append(RepairTarget(
                    file_path=test_file,
                    function_name=test_name,
                    issue=f"Test {test_name} is failing",
                    suggested_fix=(
                        "Fix the code under test, not the test itself, "
                        "unless the test has a bug."
                    ),
                ))

    # ── Fallback: add all changed files ──────────────────
    if not targets:
        for f in changed_files:
            if f not in seen_files:
                targets.append(RepairTarget(
                    file_path=f,
                    issue=diagnosis.root_cause_summary,
                    suggested_fix="Review this file for issues.",
                ))

    return targets


# ── Build Repair Plan ────────────────────────────────────

def build_repair_plan(
    diagnosis: DiagnosisReport,
    targets: list[RepairTarget],
    retry_count: int = 0,
    budget: int = 3,
) -> RepairPlan:
    """Select repair strategy and prioritize targets.

    Strategy selection follows DDI (Diminishing Debug Impact) research:
    - Retries 0-1: "incremental" — patch the specific issues.
    - Retries 2+: "fresh_start" — effectiveness drops 60-80% after 2-3
      attempts with incremental fixes, so a fresh approach is better.
    - If budget is exhausted, always use "incremental" for the final
      attempt to maximize chance of partial credit.

    Args:
        diagnosis: The failure diagnosis.
        targets: Localized repair targets.
        retry_count: How many repair attempts have been made (0-indexed).
        budget: Maximum retry budget (used to detect final attempt).

    Returns:
        A :class:`RepairPlan` with strategy and priority ordering.
    """
    # ── Strategy selection (DDI decay) ───────────────────
    is_final_attempt = retry_count >= budget - 1
    if is_final_attempt:
        # Last chance: go incremental for partial credit
        strategy = "incremental"
    elif retry_count >= 2:
        # DDI decay: incremental fixes lose 60-80% effectiveness
        # after 2-3 attempts. Switch to fresh start.
        strategy = "fresh_start"
    else:
        strategy = "incremental"

    # ── Priority ordering ────────────────────────────────
    # Prioritize by: (1) non-test files first, (2) files with line refs
    def priority_key(idx: int) -> tuple[int, int, str]:
        t = targets[idx]
        is_test = 1 if "test" in (t.file_path or "").lower() else 0
        has_line = 0 if t.line_range else 1
        return (is_test, has_line, t.file_path or "")

    priority_order = sorted(range(len(targets)), key=priority_key)

    # ── Augment suggested_fix based on category ──────────
    for t in targets:
        if not t.suggested_fix:
            t.suggested_fix = _suggest_fix_for_category(diagnosis.category, t)

    return RepairPlan(
        diagnosis=diagnosis,
        targets=targets,
        strategy=strategy,
        priority_order=priority_order,
    )


def _suggest_fix_for_category(
    category: FailureCategory,
    target: RepairTarget,
) -> str:
    """Generate a generic fix suggestion based on failure category."""
    suggestions = {
        FailureCategory.TEST_FAILURE: (
            "Review the failing assertion and fix the implementation to "
            "match expected behavior."
        ),
        FailureCategory.LOGIC_ERROR: (
            "Trace the logic flow and verify edge cases. Check "
            "conditional branches and loop boundaries."
        ),
        FailureCategory.MISSING_REQUIREMENT: (
            "Implement the missing functionality as described in the "
            "requirements."
        ),
        FailureCategory.INTERFACE_MISMATCH: (
            "Check function signatures, return types, and import paths. "
            "Ensure they match the expected API."
        ),
        FailureCategory.QUALITY_GATE: (
            "Fix lint errors and code quality issues. Run the linter "
            "locally before resubmitting."
        ),
        FailureCategory.STYLE_ISSUE: (
            "Apply consistent formatting and naming conventions."
        ),
        FailureCategory.UNKNOWN: (
            "Review the error output carefully and address the root cause."
        ),
    }
    return suggestions.get(category, suggestions[FailureCategory.UNKNOWN])


# ── Format Repair Prompt ─────────────────────────────────

def format_repair_prompt(plan: RepairPlan) -> str:
    """Format a repair plan as structured Markdown for builder injection.

    Produces a clear, actionable prompt that tells the builder agent
    exactly what to fix, where, and how.

    Args:
        plan: The repair plan to format.

    Returns:
        Markdown string suitable for prepending to the builder prompt.
    """
    lines: list[str] = []
    d = plan.diagnosis

    # ── Header ───────────────────────────────────────────
    lines.append("## Repair Instructions")
    lines.append("")
    lines.append(f"**Strategy:** {plan.strategy}")
    if plan.strategy == "fresh_start":
        lines.append(
            "> Previous incremental fixes have not resolved the issue. "
            "Take a fresh approach to the failing component."
        )
    lines.append("")

    # ── Diagnosis ────────────────────────────────────────
    lines.append("### Diagnosis")
    lines.append(f"- **Category:** {d.category.value}")
    lines.append(f"- **Root Cause:** {d.root_cause_summary}")
    lines.append(f"- **Confidence:** {d.confidence:.0%}")
    lines.append("")

    if d.evidence:
        lines.append("### Evidence")
        for e in d.evidence:
            lines.append(f"- {e}")
        lines.append("")

    # ── Repair Targets ───────────────────────────────────
    if plan.targets:
        lines.append("### Repair Targets (in priority order)")
        lines.append("")
        for idx in plan.priority_order:
            if idx >= len(plan.targets):
                continue
            t = plan.targets[idx]
            loc_parts = [f"`{t.file_path}`"]
            if t.function_name:
                loc_parts.append(f"function `{t.function_name}`")
            if t.line_range:
                loc_parts.append(f"lines {t.line_range[0]}-{t.line_range[1]}")

            lines.append(f"**{idx + 1}. {' > '.join(loc_parts)}**")
            if t.issue:
                lines.append(f"  - Issue: {t.issue}")
            if t.suggested_fix:
                lines.append(f"  - Fix: {t.suggested_fix}")
            lines.append("")

    # ── Instructions ─────────────────────────────────────
    lines.append("### Instructions")
    if plan.strategy == "fresh_start":
        lines.append(
            "1. Re-read the original requirements carefully.\n"
            "2. Discard the current approach for the failing component.\n"
            "3. Implement a fresh solution addressing all evidence above.\n"
            "4. Run tests locally before submitting."
        )
    else:
        lines.append(
            "1. Address each repair target in the priority order above.\n"
            "2. Fix the root cause, not just the symptoms.\n"
            "3. Verify your changes resolve the failing tests.\n"
            "4. Ensure lint checks pass."
        )

    return "\n".join(lines)
